        BLL.BLLSanPham.DoDuLieucbbMauSanPham(cbbmau);

        DAO.DAOKho.CountKho();
        if (countKho % 13 == 0) {
            soTrangKho = countKho / 13;
        } else {
            if (countKho < 13) {
                soTrangKho = 1;
                btnLastK.setEnabled(false);
                btnNextK.setEnabled(false);
            } else {
                soTrangKho = countKho / 13 + 1;
            }

        }
